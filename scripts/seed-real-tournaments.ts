async function main() {
  console.log('ðŸ“ Seeding REAL pickleball tournaments...')

  // Delete existing tournaments to start fresh
  await prisma.tournamentRegistration.deleteMany({})
  await prisma.tournament.deleteMany({})
  console.log('âœ“ Cleared existing tournaments')

  // Create tournaments
  for (const tournament of REAL_TOURNAMENTS) {
    await prisma.tournament.create({
      data: tournament,
    })
    console.log(`âœ“ Created: ${tournament.name}`)
  }

  console.log(`\nðŸŽ‰ Successfully seeded ${REAL_TOURNAMENTS.length} REAL tournaments!`)
  console.log('\nTournaments by state:')
  
  const byState = REAL_TOURNAMENTS.reduce((acc, t) => {
    acc[t.state] = (acc[t.state] || 0) + 1
    return acc
  }, {} as Record<string, number>)
  
  Object.entries(byState).forEach(([state, count]) => {
    console.log(`  ${state}: ${count}`)
  })
}

main()
  .catch((e) => {
    console.error('Error seeding tournaments:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
